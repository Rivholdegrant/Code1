    void arrayToModel(DefaultListModel model, Object[] array)
    {
        try
        {
            if(array.length==0);
        }catch(NullPointerException e)
        {
            return;
        }
        for(int i=0; i<array.length ; i++)
        {
            model.addElement(array[i]);
        }
    }
